package kr.or.ddit.study01;

/**
 * 단축키 클래스 입니다
 */
public class ShortCut {
	
	public static void main(String[] args) {
		System.out.println("단축기 입력 클래스입니다.");
		// 한줄 주석
		/*
		 * 여러줄 주석
		 */
		/**
		 * doc 주석
		 */
		
		/*
		 * CTRL + SHIFT + L : 이클립스 모든 단축키 안내
		 * CTRL + SHIFT +/ : 블록을 주석처리 
		 * CTRL + SHIFT +\ : 블록을 주석해제 
		 * CTRL + / : 한줄 주석 처리/해제
		 * CTRL + SHIFT + C : 한줄 주석 처리 /해제 
		 * CTRL + F : 키워드검색
		 * CTRL + H : 프로젝트 전체에서 검색
		 * CTRL + Z : 실행취소
		 * CTRL + Y : 실행취소의 취소
		 * CTRL + S : 소스파일 저장
		 * CTRL + SHIFT + S : 소스파일 전체 저장 (옆 클래스도 같이 저장)
		 * CTRL + SHIFT + X : 대문자 변환(윈도우 기본키랑 충돌. 커스텀해야댐. 현재는 캡처기능 !<- 꿀팁인듯)
		 * CTRL + SHIFT + Y : 소문자 변환
		 * CTRL + SHIFT + O : import 소환
		 * ###(중요하다는 뜻)CTRL + SPACE : 자동완성기능
		 * #CTRL + D: 한줄 삭제 (블록 삭제)
		 * #CTRL + ALT + 방향키 : 한줄 복사 (위아래 둘다 가능)
		 * #CTRL + SHIFT + F : 자동 정렬 (줄 맞춤 중요함,,)
		 * ALT + SHIFT + R : 변수 이름 수정
		 * ALT + 방향키 : 해당 줄을 이동시키기
		 * CTRL + N : 클래스 등 생성
		 */ 
		
		
		String s1 = "테스트";
		System.out.println("테스트");
		System.out.println(s1);
		System.out.println(s1);
		System.out.println(s1);
		System.out.println(s1);
		System.out.println(s1);
		System.out.println(s1);

	}
}